#ROMAN NUMERAL THING

[int]$Number = Read-Host "Enter a number between 1-10"

#If number is 1, output I, and so on

if ($Number -eq 1) {
    Write-Host "I" 
}

if ($Number -eq 2) {
    Write-Host "II" 
}

if ($Number -eq 3) {
    Write-Host "III" 
}

if ($Number -eq 4) {
    Write-Host "IV" 
}

if ($Number -eq 5) {
    Write-Host "V" 
}

if ($Number -eq 6) {
    Write-Host "VI" 
}

if ($Number -eq 7) {
    Write-Host "VII" 
}

if ($Number -eq 8) {
    Write-Host "VIII" 
}

if ($Number -eq 9) {
    Write-Host "IX" 
}

if ($Number -eq 10) {
    Write-Host "X" 
}

Write-Host "Thanks for playing."
